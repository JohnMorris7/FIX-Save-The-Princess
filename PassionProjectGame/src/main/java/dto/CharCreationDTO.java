package dto;

public class CharCreationDTO {

    String name;
    Integer defense;
    Integer attack;
    Integer stamina;
    Integer speed;
    Integer health;
    String armorName;
    Integer armorDefense;
    String weaponName;
    Integer weaponAttack;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getDefense() {
        return defense;
    }

    public void setDefense(Integer defense) {
        this.defense = defense;
    }

    public Integer getAttack() {
        return attack;
    }

    public void setAttack(Integer attack) {
        this.attack = attack;
    }

    public Integer getStamina() {
        return stamina;
    }

    public void setStamina(Integer stamina) {
        this.stamina = stamina;
    }

    public Integer getSpeed() {
        return speed;
    }

    public void setSpeed(Integer speed) {
        this.speed = speed;
    }

    public Integer getHealth() {
        return health;
    }

    public void setHealth(Integer health) {
        this.health = health;
    }

    public String getArmorName() {
        return armorName;
    }

    public void setArmorName(String armorName) {
        this.armorName = armorName;
    }

    public Integer getArmorDefense() {
        return armorDefense;
    }

    public void setArmorDefense(Integer armorDefense) {
        this.armorDefense = armorDefense;
    }

    public String getWeaponName() {
        return weaponName;
    }

    public void setWeaponName(String weaponName) {
        this.weaponName = weaponName;
    }

    public Integer getWeaponAttack() {
        return weaponAttack;
    }

    public void setWeaponAttack(Integer weaponAttack) {
        this.weaponAttack = weaponAttack;
    }
}
